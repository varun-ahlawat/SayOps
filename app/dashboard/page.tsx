"use client"

import React, { useEffect, useState } from "react"
import { useSearchParams, useRouter } from "next/navigation"
import { Suspense } from "react"
import { AppSidebar } from "@/components/app-sidebar"
import { ChartAreaInteractive } from "@/components/chart-area-interactive"
import { SectionCards } from "@/components/section-cards"
import { SiteHeader } from "@/components/site-header"
import { CallHistoryTable } from "@/components/call-history-table"
import {
  SidebarInset,
  SidebarProvider,
} from "@/components/ui/sidebar"
import {
  Card,
  CardDescription,
  CardHeader,
  CardTitle,
} from "@/components/ui/card"
import { Badge } from "@/components/ui/badge"
import { IconRobot } from "@tabler/icons-react"
import { useAuth } from "@/lib/auth-context"
import { fetchAgents, fetchCalls, fetchStats } from "@/lib/api-client"
import type { Agent, CallHistoryEntryWithTurns, DashboardStats } from "@/lib/types"

function DashboardContent() {
  const searchParams = useSearchParams()
  const router = useRouter()
  const { user, loading: authLoading } = useAuth()

  const agentId = searchParams.get("agent") || ""

  const [agents, setAgents] = useState<Agent[]>([])
  const [calls, setCalls] = useState<CallHistoryEntryWithTurns[]>([])
  const [stats, setStats] = useState<DashboardStats | null>(null)
  const [loading, setLoading] = useState(true)

  const selectedAgent = agentId
    ? agents.find((a) => a.id === agentId) || agents[0]
    : agents[0]

  useEffect(() => {
    if (authLoading) return
    if (!user) {
      router.push("/login")
      return
    }

    async function load() {
      try {
        const [agentsData, statsData] = await Promise.all([
          fetchAgents(),
          fetchStats(),
        ])

        // Redirect to create-agent if user has no agents
        if (agentsData.length === 0) {
          router.push("/create-agent")
          return
        }

        setAgents(agentsData)
        setStats(statsData)

        // Load calls for the selected agent (or first agent)
        const targetAgentId = agentId || agentsData[0]?.id
        if (targetAgentId) {
          const callsData = await fetchCalls(targetAgentId)
          setCalls(callsData)
        }
      } catch (err) {
        console.error("Failed to load dashboard data:", err)
      } finally {
        setLoading(false)
      }
    }

    load()
  }, [user, authLoading, agentId, router])

  if (authLoading || loading) {
    return (
      <div className="flex min-h-screen items-center justify-center">
        <p className="text-muted-foreground">Loading dashboard...</p>
      </div>
    )
  }

  if (!user) return null

  return (
    <SidebarProvider
      style={
        {
          "--sidebar-width": "18rem",
          "--header-height": "3rem",
        } as React.CSSProperties
      }
    >
      <AppSidebar variant="inset" />
      <SidebarInset>
        <SiteHeader />
        <div className="flex flex-1 flex-col gap-4 p-4 md:gap-6 lg:p-6">
          {/* Agent Status Card */}
          {selectedAgent && (
            <Card>
              <CardHeader>
                <div className="flex items-center justify-between">
                  <div className="flex items-center gap-3">
                    <div className="flex size-10 items-center justify-center rounded-lg bg-primary text-primary-foreground">
                      <IconRobot className="size-5" />
                    </div>
                    <div>
                      <CardTitle>{selectedAgent.name}</CardTitle>
                      <CardDescription className="font-mono text-xs">
                        {selectedAgent.phone_number || "No phone number assigned"}
                      </CardDescription>
                    </div>
                  </div>
                  <Badge variant={selectedAgent.status === "active" ? "default" : "secondary"}>
                    {selectedAgent.status}
                  </Badge>
                </div>
              </CardHeader>
            </Card>
          )}

          {/* Stats */}
          {stats && <SectionCards stats={stats} agents={agents} />}

          {/* Call Volume Chart */}
          {stats && <ChartAreaInteractive data={stats.calls_per_day} agents={agents} />}

          {/* Recent Calls */}
          <CallHistoryTable calls={calls.slice(0, 5)} />
        </div>
      </SidebarInset>
    </SidebarProvider>
  )
}

export default function Page() {
  return (
    <Suspense>
      <DashboardContent />
    </Suspense>
  )
}
